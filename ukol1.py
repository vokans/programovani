# vytvářím proměnnou 'x', a říkám, že v ní bude číslo 4
x = 4

# napíšu hodnotu proměnné 'x'
print(x)
# print je z angličtiny, znamená to vytisknout

# to, že 'x' je proměnná znamená, že se její hodnota může měnit
x = "ahoj"
# abys viděl, že je to pravda, znovu napíšu hodnotu 'x':
print(x)
# a už je tam napsaný něco jinýho než 4, žejo?

# většinou asi budeš používat proměnné, které mají stejný typ
# třeba čísla:
cislo1 = 42
cislo2 = 7
cislo3 = cislo1 / cislo2
print(cislo3)
# víš, co se napíše teď?

